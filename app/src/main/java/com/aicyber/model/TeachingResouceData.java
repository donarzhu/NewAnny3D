package com.aicyber.model;

public class TeachingResouceData {
	private String start_way = "";
	private String lesson_id = "";
	private String unit_id = "";
	private String extra_operation = "";
	private String yes = "";
	private String no = "";

	public String getYes() {
		return yes;
	}

	public void setYes(String yes) {
		this.yes = yes;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getExtra_operation() {
		return extra_operation;
	}

	public void setExtra_operation(String extra_operation) {
		this.extra_operation = extra_operation;
	}

	public String getStart_way() {
		return start_way;
	}

	public void setStart_way(String start_way) {
		this.start_way = start_way;
	}

	public String getLesson_id() {
		return lesson_id;
	}

	public void setLesson_id(String lesson_id) {
		this.lesson_id = lesson_id;
	}

	public String getUnit_id() {
		return unit_id;
	}

	public void setUnit_id(String unit_id) {
		this.unit_id = unit_id;
	}
}
